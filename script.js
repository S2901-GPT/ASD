function renderPoints(item) {
  const points = getVisiblePoints(item);
  if (!points.length) {
    return `<div class="empty-state">لا توجد نتائج مطابقة للبحث أو الفلتر الحالي.</div>`;
  }

  return `
    <div class="points-grid">
      ${points.map(point => {
        const type = typeInfo[point.type];
        return `
          <article class="point-card" style="--type-color:${type.color}">
            <span class="type-pill">${type.label}</span>
            <h4>${highlight(point.text)}</h4>
            <p>${highlight(point.example)}</p>
          </article>
        `;
      }).join("")}
    </div>
  `;
}

function bindEvents() {
  document.querySelectorAll(".nav-button").forEach(button => {
    button.addEventListener("click", () => {
      state.activeId = button.dataset.id;
      renderApp();
    });
  });

  document.querySelectorAll(".filter-button").forEach(button => {
    button.addEventListener("click", () => {
      state.filter = button.dataset.filter;
      renderApp();
    });
  });

  const searchBox = document.getElementById("searchBox");
  searchBox.addEventListener("input", event => {
    state.search = event.target.value;
    renderApp();
    const nextSearchBox = document.getElementById("searchBox");
    nextSearchBox.focus();
    nextSearchBox.setSelectionRange(nextSearchBox.value.length, nextSearchBox.value.length);
  });
}

window.addEventListener("DOMContentLoaded", renderApp);
