function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/\"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function highlight(value) {
  const safe = escapeHtml(value);
  const query = state.search.trim();
  if (!query) return safe;
  const escapedQuery = query.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return safe.replace(new RegExp(escapedQuery, "gi"), match => `<mark>${match}</mark>`);
}

function countAll(type) {
  return DATA.reduce((sum, item) => sum + item.points.filter(point => point.type === type).length, 0);
}

function getActiveItem() {
  return DATA.find(item => item.id === state.activeId) || DATA[0];
}

function getVisiblePoints(item) {
  const query = state.search.trim().toLowerCase();
  return item.points.filter(point => {
    const byFilter = state.filter === "all" || point.type === state.filter;
    const bySearch = !query || `${item.title} ${item.group} ${point.text} ${point.example}`.toLowerCase().includes(query);
    return byFilter && bySearch;
  });
}

